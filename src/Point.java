public class Point {

    //Q1 a
    private double x;
    private double y;

    //Q1 b
    Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getX() {
        return this.x;
    }

    public double getY() {
        return y;
    }
} // end of Point class
